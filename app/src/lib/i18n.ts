interface LocaleStore {
    locale: Locale;
    setLocale: (l: Locale) => void;
}

export const useLocale = create<LocaleStore>()(
    persist(
        (set) => ({
            locale: "de",
            setLocale: (locale) => set({ locale }),
        }),
        { name: "medoc-locale" },
    ),
);

/// Translate a key for the current locale, falling back to the key itself.
export function t(key: string): string {
    const { locale } = useLocale.getState();
    return translateLocale(locale, key);
}

/** Explicit locale (effects, native menu sync) — same fallbacks as {@link useT}. */
export function translateLocale(locale: Locale, key: string): string {
    return dict[locale][key] ?? dict.de[key] ?? key;
}

/// React hook variant for components that need to re-render on locale change.
export function useT() {
    const locale = useLocale((s) => s.locale);
    return (key: string) => translateLocale(locale, key);
}
